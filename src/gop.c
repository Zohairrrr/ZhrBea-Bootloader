/* ── GOP init: find best mode ────────────────────────────────────────────── */
EFI_STATUS gop_init(EFI_SYSTEM_TABLE *ST)
{
    EFI_GUID gopGuid = EFI_GRAPHICS_OUTPUT_PROTOCOL_GUID;
    EFI_STATUS s = uefi_call_wrapper(gBS->LocateProtocol, 3,
                                     &gopGuid, NULL, (void**)&g_gop.gop);
    if (EFI_ERROR(s)) { g_gop.ready = FALSE; return s; }

    EFI_GRAPHICS_OUTPUT_PROTOCOL *gop = g_gop.gop;

    /* Find best mode: prefer 1024x768, then 800x600, else keep current */
    UINT32 best_mode = gop->Mode->Mode;
    UINT32 best_w = gop->Mode->Info->HorizontalResolution;
    UINT32 best_h = gop->Mode->Info->VerticalResolution;
    BOOLEAN found_preferred = FALSE;

    UINT32 m;
    for (m = 0; m < gop->Mode->MaxMode; m++) {
        EFI_GRAPHICS_OUTPUT_MODE_INFORMATION *info;
        UINTN sz;
        if (EFI_ERROR(uefi_call_wrapper(gop->QueryMode, 4, gop, m, &sz, &info)))
            continue;
        if (info->PixelFormat == PixelBltOnly) continue;

        UINT32 w = info->HorizontalResolution;
        UINT32 h = info->VerticalResolution;

        /* Prefer 1024x768 exactly */
        if (w == 1024 && h == 768) {
            best_mode = m; best_w = w; best_h = h;
            found_preferred = TRUE; break;
        }
        /* Fall back to 800x600 */
        if (!found_preferred && w == 800 && h == 600) {
            best_mode = m; best_w = w; best_h = h;
        }
    }

    uefi_call_wrapper(gop->SetMode, 2, gop, best_mode);

    g_gop.fb     = (UINT32*)gop->Mode->FrameBufferBase;
    g_gop.fb_size= gop->Mode->FrameBufferSize;
    g_gop.width  = gop->Mode->Info->HorizontalResolution;
    g_gop.height = gop->Mode->Info->VerticalResolution;
    g_gop.stride = gop->Mode->Info->PixelsPerScanLine;
    g_gop.ready  = TRUE;
    return EFI_SUCCESS;
}

/* ── Pixel ops ───────────────────────────────────────────────────────────── */
static inline UINT32 _pack(Pixel p) {
    return ((UINT32)p.r << 16) | ((UINT32)p.g << 8) | (UINT32)p.b;
}

void gop_put_pixel(UINT32 x, UINT32 y, Pixel c) {
    if (!g_gop.ready || x >= g_gop.width || y >= g_gop.height) return;
    g_gop.fb[y * g_gop.stride + x] = _pack(c);
}

void gop_clear(Pixel c) {
    UINT32 val = _pack(c);
    UINT32 x, y;
    /* Fill first scanline */
    for (x = 0; x < g_gop.width; x++)
        g_gop.fb[x] = val;
    /* Copy first scanline to all others using UEFI CopyMem */
    for (y = 1; y < g_gop.height; y++)
        uefi_call_wrapper(gBS->CopyMem, 3,
            g_gop.fb + y * g_gop.stride,
            g_gop.fb,
            g_gop.width * sizeof(UINT32));
}

void gop_fill_rect(UINT32 x, UINT32 y, UINT32 w, UINT32 h, Pixel c) {
    if (!g_gop.ready) return;
    UINT32 val = _pack(c);
    UINT32 xi, yi;
    UINT32 x2 = (x + w > g_gop.width)  ? g_gop.width  : x + w;
    UINT32 y2 = (y + h > g_gop.height) ? g_gop.height : y + h;
    for (yi = y; yi < y2; yi++)
        for (xi = x; xi < x2; xi++)
            g_gop.fb[yi * g_gop.stride + xi] = val;
}

Pixel gop_blend(Pixel src, Pixel dst, UINT8 alpha) {
    UINT32 a = alpha, ia = 255 - alpha;
    Pixel r;
    r.r = (UINT8)((src.r * a + dst.r * ia) / 255);
    r.g = (UINT8)((src.g * a + dst.g * ia) / 255);
    r.b = (UINT8)((src.b * a + dst.b * ia) / 255);
    r.a = 0xFF;
    return r;
}

void gop_fill_rect_alpha(UINT32 x, UINT32 y, UINT32 w, UINT32 h, Pixel c, UINT8 alpha) {
    if (!g_gop.ready) return;
    UINT32 xi, yi;
    UINT32 x2 = (x+w > g_gop.width)  ? g_gop.width  : x+w;
    UINT32 y2 = (y+h > g_gop.height) ? g_gop.height : y+h;
    for (yi = y; yi < y2; yi++) {
        for (xi = x; xi < x2; xi++) {
            UINT32 old = g_gop.fb[yi * g_gop.stride + xi];
            Pixel dst = { (UINT8)(old), (UINT8)(old>>8), (UINT8)(old>>16), 0xFF };
            Pixel blended = gop_blend(c, dst, alpha);
            g_gop.fb[yi * g_gop.stride + xi] = _pack(blended);
        }
    }
}

void gop_draw_rect(UINT32 x, UINT32 y, UINT32 w, UINT32 h, UINT32 t, Pixel c) {
    gop_fill_rect(x,       y,       w, t, c);
    gop_fill_rect(x,       y+h-t,   w, t, c);
    gop_fill_rect(x,       y,       t, h, c);
    gop_fill_rect(x+w-t,   y,       t, h, c);
}

void gop_draw_hline_px(UINT32 x, UINT32 y, UINT32 len, UINT32 t, Pixel c) {
    gop_fill_rect(x, y, len, t, c);
}

/* ── Font rendering ──────────────────────────────────────────────────────── */
UINT32 gop_char_width(void)  { return 8; }
UINT32 gop_char_height(void) { return 16; }

void gop_draw_char(UINT32 x, UINT32 y, char ch, Pixel fg, Pixel bg, BOOLEAN transp) {
    if (!g_gop.ready) return;
    UINT8 idx = (UINT8)ch;
    if (idx < 0x20 || idx > 0x7E) idx = 0x20;
    const UINT8 *glyph = font8x16[idx - 0x20];
    UINT32 row, col;
    for (row = 0; row < 16; row++) {
        UINT8 bits = glyph[row];
        for (col = 0; col < 8; col++) {
            if (bits & (0x80 >> col))
                gop_put_pixel(x+col, y+row, fg);
            else if (!transp)
                gop_put_pixel(x+col, y+row, bg);
        }
    }
}

void gop_draw_str(UINT32 x, UINT32 y, const char *s, Pixel fg, Pixel bg, BOOLEAN transp) {
    while (*s) {
        gop_draw_char(x, y, *s, fg, bg, transp);
        x += 8; s++;
    }
}

UINT32 gop_str_width(const char *s) {
    UINT32 n = 0; while (*s++) n++; return n * 8;
}

void gop_draw_wstr(UINT32 x, UINT32 y, const CHAR16 *s, Pixel fg, Pixel bg, BOOLEAN transp) {
    while (*s) {
        char c = (*s < 0x80) ? (char)*s : '?';
        gop_draw_char(x, y, c, fg, bg, transp);
        x += 8; s++;
    }
}

UINT32 gop_wstr_width(const CHAR16 *s) {
    UINT32 n = 0; while (*s++) n++; return n * 8;
}